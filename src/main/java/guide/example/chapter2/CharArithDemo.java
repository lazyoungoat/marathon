package guide.example.chapter2;

public class CharArithDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("ch = " + ch);

        ch ++;
        System.out.println("ch = " + ch);

//        char uses Unicode which includes ASCII
        ch = 80;
        System.out.println("ch = " + ch);





    }
}
